import fs from "node:fs";
import path from "node:path";

export function buildRegressionContract(root, framework, findings) {
  const routes = [];
  const apis = [];
  if (fs.existsSync(root)) {
    for (const f of walk(root)) {
      if (f.match(/app\/.*\/page\.(tsx|jsx)$/)) routes.push("/" + f.replace(/^app\//, "").replace(/\/page\.(tsx|jsx)$/, "").replace(/\/page$/, "") || "/");
      if (f.match(/app\/api\/.*\/route\.(ts|js)$/)) {
        const seg = f.replace(/^app\/api\//, "").replace(/\/route\.(ts|js)$/, "");
        apis.push(`/api/${seg}`);
      }
      if (f.match(/pages\/api\/.*\.(ts|js)$/)) {
        apis.push("/" + f.replace(/^pages/, "").replace(/\.(ts|js)$/, ""));
      }
    }
  }

  const pkg = readJson(path.join(root, "package.json"));
  const buildCmd = pkg?.scripts?.build ? "npm run build" : "npm run build";
  const testCmd = pkg?.scripts?.test ? "npm run test" : null;

  const safeDeletes = findings.filter((f) => f.action === "SAFE_DELETE");

  return {
    title: "Regression Contract",
    must_pass: {
      build_command: buildCmd,
      test_command: testCmd,
      routes_to_load: [...new Set(routes)].slice(0, 20),
      api_endpoints: [...new Set(apis)].slice(0, 20),
      auth_pages: routes.filter((r) => /login|auth|signup/i.test(r)),
    },
    manual_qa: [
      "Homepage loads without 500",
      "Primary navigation links work",
      "Auth flow completes (if applicable)",
      "API health endpoint returns 200",
      "No console errors on critical paths",
      "Build succeeds locally after patch",
    ],
    after_cleanup_verify: safeDeletes.map((f) => `Confirm removed: ${f.file_path || f.package} — ${f.reason}`),
    do_not_delete: findings.filter((f) => f.action === "DO_NOT_TOUCH").map((f) => f.file_path),
  };
}

export function regressionMarkdown(contract) {
  const lines = [
    "# Regression Contract",
    "",
    "Generated by RepoDiet. Verify these before merging any cleanup patch.",
    "",
    "## Build",
    "",
    "```bash",
    contract.must_pass.build_command,
    "```",
    "",
  ];
  if (contract.must_pass.test_command) {
    lines.push("## Test", "", "```bash", contract.must_pass.test_command, "```", "");
  }
  lines.push("## Routes that must load", "");
  for (const r of contract.must_pass.routes_to_load) lines.push(`- [ ] ${r || "/"}`);
  lines.push("", "## API endpoints", "");
  for (const a of contract.must_pass.api_endpoints) lines.push(`- [ ] ${a}`);
  lines.push("", "## Manual QA", "");
  for (const q of contract.manual_qa) lines.push(`- [ ] ${q}`);
  lines.push("", "## Post-cleanup checks", "");
  for (const c of contract.after_cleanup_verify.slice(0, 30)) lines.push(`- [ ] ${c}`);
  return lines.join("\n");
}

