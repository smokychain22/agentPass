export function generatePatch(findings, root) {
  const lines = ["# RepoDiet cleanup.patch", "# Safe-delete operations — apply with git apply or manual review", ""];
  const safe = findings.filter((f) => f.action === "SAFE_DELETE");

  for (const f of safe) {
    if (f.type === "unused_dependency" && f.package) {
      lines.push(`# REMOVE dependency: ${f.package}`);
      lines.push(`--- a/package.json`);
      lines.push(`+++ b/package.json`);
      lines.push(`@@ remove unused dependency ${f.package}`);
      lines.push("");
      continue;
    }
    if (f.type === "dead_file" && f.file_path && f.file_path !== "package.json") {
      const abs = path.join(root, f.file_path);
      if (fs.existsSync(abs)) {
        lines.push(`diff --git a/${f.file_path} b/${f.file_path}`);
        lines.push(`deleted file mode 100644`);
        lines.push(`--- a/${f.file_path}`);
        lines.push(`+++ /dev/null`);
        const content = fs.readFileSync(abs, "utf8").split("\n");
        lines.push(`@@ -1,${content.length} +0,0 @@`);
        for (const line of content.slice(0, 50)) {
          lines.push(`-${line}`);
        }
        if (content.length > 50) lines.push(`# ... truncated (${content.length} lines total)`);
        lines.push("");
      }
    }
  }

  if (lines.length <= 4) {
    lines.push("# No automatic safe deletes — review REVIEW_FIRST findings manually.");
  }
  return lines.join("\n");
}

export function beforeAfterDelta(before, after) {
  return {
    bloat_score: { before: before.bloat_score, after: after.bloat_score, delta: before.bloat_score - after.bloat_score },
    unused_files: { before: before.unused_files, after: after.unused_files },
    unused_dependencies: { before: before.unused_dependencies, after: after.unused_dependencies },
    duplicate_clusters: { before: before.duplicate_clusters, after: after.duplicate_clusters },
  };
}

function walk(root, rel = "") {
  const out = [];
  if (!fs.existsSync(root)) return out;
  for (const ent of fs.readdirSync(root, { withFileTypes: true })) {
    if (["node_modules", ".git", ".next"].includes(ent.name)) continue;
    const relPath = rel ? `${rel}/${ent.name}` : ent.name;
    const abs = path.join(root, relPath);
    if (ent.isDirectory()) out.push(...walk(abs, relPath));
    else out.push(relPath);
  }
  return out;
}

function readJson(p) {
  try {
    return JSON.parse(fs.readFileSync(p, "utf8"));
  } catch {
    return null;
  }
}
